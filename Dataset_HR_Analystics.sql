DROP TABLE IF EXISTS leavers CASCADE;
DROP TABLE IF EXISTS engagement_surveys CASCADE;
DROP TABLE IF EXISTS performance_reviews CASCADE;
DROP TABLE IF EXISTS salaries CASCADE;
DROP TABLE IF EXISTS employees CASCADE;
DROP TABLE IF EXISTS job_titles CASCADE;
DROP TABLE IF EXISTS departments CASCADE;

-- 1. DEPARTMENTS
CREATE TABLE departments (
  dept_id SERIAL PRIMARY KEY,
  dept_name TEXT NOT NULL,
  location TEXT NOT NULL,
  head_id INT NULL
);

-- 2. JOB_TITLES
CREATE TABLE job_titles (
  title_id SERIAL PRIMARY KEY,
  title_name TEXT NOT NULL,
  level TEXT CHECK(level IN ('Junior','Mid','Senior','Lead','Manager','Director')),
  track TEXT CHECK(track IN ('Engineering','Product','Design','HR','Finance','Sales'))
);

-- 3. EMPLOYEES
CREATE TABLE employees (
  emp_id SERIAL PRIMARY KEY,
  first_name TEXT NOT NULL,
  last_name TEXT NOT NULL,
  gender CHAR(1) CHECK(gender IN ('M','F','O')),
  date_of_birth DATE NOT NULL,
  hired_date DATE NOT NULL,
  dept_id INT REFERENCES departments(dept_id),
  title_id INT REFERENCES job_titles(title_id),
  manager_id INT REFERENCES employees(emp_id),
  employment_status TEXT CHECK(employment_status IN ('active','on_leave','terminated')),
  city TEXT NOT NULL,
  remote_status TEXT CHECK(remote_status IN ('Remote','Hybrid','Onsite')),
  education_level TEXT
);

-- 4. SALARIES
CREATE TABLE salaries (
  salary_id SERIAL PRIMARY KEY,
  emp_id INT REFERENCES employees(emp_id),
  base_salary NUMERIC(12,2) NOT NULL,
  bonus NUMERIC(12,2),
  effective_date DATE NOT NULL
);

-- 5. PERFORMANCE_REVIEWS
CREATE TABLE performance_reviews (
  review_id SERIAL PRIMARY KEY,
  emp_id INT REFERENCES employees(emp_id),
  review_date DATE NOT NULL,
  performance_score INT CHECK(performance_score BETWEEN 1 AND 10),
  rating TEXT CHECK(rating IN ('Below expectation','Meets expectation','Exceeds expectation')),
  promo_recommendation BOOLEAN
);

-- 6. ENGAGEMENT_SURVEYS
CREATE TABLE engagement_surveys (
  response_id SERIAL PRIMARY KEY,
  emp_id INT REFERENCES employees(emp_id),
  survey_date DATE NOT NULL,
  engagement_score INT CHECK(engagement_score BETWEEN 1 AND 10),
  wellbeing_score INT CHECK(wellbeing_score BETWEEN 1 AND 10),
  nps_score INT CHECK(nps_score BETWEEN -100 AND 100)
);

-- 7. LEAVERS
CREATE TABLE leavers (
  leaver_id SERIAL PRIMARY KEY,
  emp_id INT REFERENCES employees(emp_id),
  leaving_date DATE NOT NULL,
  reason TEXT,
  exit_type TEXT CHECK(exit_type IN ('Voluntary','Involuntary','Retirement')),
  rehired BOOLEAN DEFAULT FALSE
);

